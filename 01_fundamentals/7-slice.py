movieName = "Top Gun"

# String [inicio:fim] - indice comeca na posicao zero | indice final -1 

# 1 - Buscar toda string a partir da primeira posicao
print(movieName[0:])

# 2 - Buscar toda string ate a ultima posicao
print(movieName[:7])

# 3 - Busca toda string da terceira ate a ultinma posicao
print(movieName[2:])


# 4 - Busca toda string de 2 em 2 caracters
print(movieName[::2])